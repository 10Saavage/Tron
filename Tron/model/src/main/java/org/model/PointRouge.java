package org.model;


/**
 * @author TOUSSI Manoel
 *
 */
public class PointRouge {

	protected int x;
	protected int y;
	/**
	 * @param x
	 * @param y
	 * @param image
	 */
	public PointRouge(int x, int y) {
		this.x = x;
		this.y = y;
	}


	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}


	/**
	 * @param x the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}


	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}


	/**
	 * @param y the y to set
	 */
	public void setY(int y) {
		this.y = y;
	}	
	
	
}
